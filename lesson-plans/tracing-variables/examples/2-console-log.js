/* Console Logging

  the console is where info is displayed for developers

  `console.log` is the simplest way to display something

  PS. Open your dev tools console to see this output!

*/

// an empty string
console.log('');

// a string with the text "a string!"
console.log('a string!');

// a string with quotes in it
console.log('cows say "moo"');

// you can log more than one value
//  just separate them with a coma
console.log('hack', 'your', 'future');
