/* String Concatenation

  you can use `+` symbol to combine two strings into one string

  study tips: use the [trace] button & [open in] jsviz2

*/

console.log('hello' + '!'); // --> 'hello!'
console.log('hel' + 'lo!'); // --> 'hello!'
console.log('h' + 'ello!'); // --> 'hello!'
console.log('h' + 'el' + 'lo' + '!'); // --> 'hello!'

